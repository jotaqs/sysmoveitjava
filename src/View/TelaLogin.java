    private void btnTLregistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTLregistrarActionPerformed

        String cod = campoCod.getText();
        String declaracao = campoDeclaracao.getText();
        String cpf = campoData.getText();
        String data = campoCpf.getText();
         
        Login l =  new Login();
        
        l.setCodRastreio(cod);
        l.setDeclaracao(declaracao);
        l.setCpf(cpf);
        l.setData(data);
        
        new Model.ConexaoDAO.ConexaoDAO().inserir(l);
        
        JOptionPane.showMessageDialog(null,"Cadastrado");
        campoCod.setText("");
        campoDeclaracao.setText("");
        campoCpf.setText("");
        campoData.setText("");
        
    }//GEN-LAST:event_btnTLregistrarActionPerformed

